package day05operators;

public class OperatorSigns {

	public static void main(String[] args) {
		
		 
		 boolean b1 = 12==20;
		 System.out.println(b1);
		 
		 System.out.println(12==20);
		 
		 System.out.println(12<20);
		 
		 System.out.println(12!=20); 
		 
		 System.out.println(12>=20);
		 
		 System.out.println(12>=12);
	}

}
