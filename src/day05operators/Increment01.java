package day05operators;

public class Increment01 {

	public static void main(String[] args) {
		// int data tipinde iki variable olusturun.Birinciyi 3, ikinciyi 7 artirin.
		//Asagidaki yöntemler uzun olanlardir. Diger class'ta kisa yol vardir.
		
	int sayi1 = 12;
	sayi1 = sayi1 + 3;
	System.out.println(sayi1);
	
	int sayi2 = 24;
	sayi2 = sayi2 + 7;
			
	System.out.println(sayi2);
	
	// int data tipinde iki variable olusturun.Birinciyi 3, ikinciyi 7 artirin.
	
	int num1 = 14;
	num1= num1 + 3;
	num1+= 3;
	System.out.println(num1);
	
	}

}
