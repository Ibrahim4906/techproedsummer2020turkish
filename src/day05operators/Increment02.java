package day05operators;

public class Increment02 {

	public static void main(String[] args) {
		
		// int. data type'inde iki tane variable olusturun. 
		
		int sayi1 = 12;
		
		sayi1+=3; // increment sayi1 = sayi1 + 3 yandaki ifade ile aynidir. 
				  // + ile = arasina bosluk birakmayin.
		System.out.println(sayi1);
		
		int sayi3 = 21;
		
		sayi3+=7;
		System.out.println(sayi3);
		
		//Asagidaki data type'i kolay yoldan 5 artirin.
		
		int num1 = 6;
		num1+=5;
		System.out.println(num1);
		
	}

}
